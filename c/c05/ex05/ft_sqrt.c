int	ft_sqrt(int nb)
{
	long	ab;

	if (nb < 0)
		return (0);
	if (nb == 1)
		return (1);
	ab = 1;
	while (ab * ab < nb)
		ab++;
	if (ab * ab == nb)
		return (ab);
	return (0);
}
