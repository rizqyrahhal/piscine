#include <stdlib.h>

char	*ft_strcpy(char *dest, char *src)
{
	int	count;

	count = 0;
	while (src[count] != '\0')
	{
		dest[count] = src[count];
		count++;
	}
	dest[count] = src[count];
	return (0);
}

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (str[i])
		i++;
	return (i);
}

char	*ft_strdup(char *src)
{
	char	*res;

	res = malloc(sizeof(char) * (ft_strlen(src) + 1));
	if (!res)
		return (0);
	ft_strcpy(res, src);
	return (res);
}
