#include <unistd.h>

void	ft_putchar(int a)
{
	write(1, &a, 1);
}

void	ft_print_numbers(void)
{
	int	a;

	a = 0;
	while (a < 10)
	{
		ft_putchar(a + 48);
		a++;
	}
}
